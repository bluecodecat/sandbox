void draw_title(const uint8_t *title, const uint8_t x) {
	int i = 0;

	send_data(CGRAM | 0x00 & CGRAM_MASK, RW_WRITE, RS_COMMAND);
	for(; i < 64; i++) send_data(title[i], RW_WRITE, RS_DATA);

	send_data(DDRAM | (0x04 + x) & DDRAM_MASK, RW_WRITE, RS_COMMAND);
	for(; i < 72; i++) send_data(title[i], RW_WRITE, RS_DATA);
	send_data(DDRAM | (0x44 + x) & DDRAM_MASK, RW_WRITE, RS_COMMAND);
	for(; i < 80; i++) send_data(title[i], RW_WRITE, RS_DATA);
}
